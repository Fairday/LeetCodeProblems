using System;
using System.Collections.Generic;
using System.Text;

namespace ALGON.LeetCodeProblems.N_aryTree
{
    public class Node
    {
        public int val;
        public IList<Node> children;

        public Node() { }

        public Node(int _val)
        {
            val = _val;
        }

        public Node(int _val, IList<Node> _children)
        {
            val = _val;
            children = _children;
        }
    }
}
